// ENDPOINT 2: Verificar c√≥digo y guardar inscripci√≥n
// ===============================
router.post('/verificar-codigo', async (req, res) => {
    try {
        const { email, codigo } = req.body;

        console.log('üîç Verificando c√≥digo para:', email);
        console.log('üîç C√≥digo recibido:', codigo);

        // Validar par√°metros
        if (!email || !codigo) {
            return res.status(400).json({
                success: false,
                message: 'Email y c√≥digo son requeridos'
            });
        }

        // Buscar c√≥digo almacenado
        const datosVerificacion = global.codigosVerificacion.get(email.toLowerCase().trim());
        
        if (!datosVerificacion) {
            return res.status(400).json({
                success: false,
                message: 'No se encontr√≥ c√≥digo para este email o ya expir√≥'
            });
        }
        
        // Verificar expiraci√≥n (10 minutos)
        if (Date.now() > datosVerificacion.expiresAt) {
            global.codigosVerificacion.delete(email.toLowerCase().trim());
            return res.status(400).json({
                success: false,
                message: 'C√≥digo expirado. Solicita uno nuevo.'
            });
        }
        
        // Verificar intentos m√°ximos (3 intentos)
        if (datosVerificacion.intentos >= 3) {
            global.codigosVerificacion.delete(email.toLowerCase().trim());
            return res.status(400).json({
                success: false,
                message: 'Demasiados intentos fallidos. Solicita un c√≥digo nuevo.'
            });
        }
        
        // Verificar c√≥digo
        if (codigo.trim() !== datosVerificacion.codigo) {
            datosVerificacion.intentos++;
            return res.status(400).json({
                success: false,
                message: `C√≥digo incorrecto. Intentos restantes: ${3 - datosVerificacion.intentos}`
            });
        }
        
        console.log('‚úÖ C√≥digo verificado correctamente');
        
        // C√≥digo correcto - proceder a guardar inscripci√≥n
        const datosInscripcion = datosVerificacion.datosInscripcion;
        
        try {
            const Inscripcion = require('../models/Inscripcion');
            
            // Validar DNI √∫nico antes de crear
            const existeDNI = await Inscripcion.findOne({ 
                dni: datosInscripcion.dni, 
                activo: true 
            });
            if (existeDNI) {
                global.codigosVerificacion.delete(email.toLowerCase().trim());
                return res.status(400).json({
                    success: false,
                    message: `El DNI ${datosInscripcion.dni} ya est√° registrado en el sistema`
                });
            }

            // Validar email √∫nico antes de crear
            const existeEmail = await Inscripcion.findOne({ 
                email: datosInscripcion.email, 
                activo: true 
            });
            if (existeEmail) {
                global.codigosVerificacion.delete(email.toLowerCase().trim());
                return res.status(400).json({
                    success: false,
                    message: `El email ${datosInscripcion.email} ya est√° registrado en el sistema`
                });
            }
            
            // Crear nueva inscripci√≥n
            const nuevaInscripcion = new Inscripcion(datosInscripcion);
            const inscripcionGuardada = await nuevaInscripcion.save();
            
            console.log('‚úÖ Inscripci√≥n guardada en DB:', inscripcionGuardada.NRO);
            
            // Enviar email de confirmaci√≥n
            try {
                await emailService.enviarConfirmacionInscripcion(email, inscripcionGuardada);
                console.log('‚úÖ Email de confirmaci√≥n enviado');
            } catch (emailError) {
                console.error('‚ö†Ô∏è Error enviando confirmaci√≥n:', emailError);
            }

            // Limpiar c√≥digo usado
            global.codigosVerificacion.delete(email.toLowerCase().trim());
            
            res.json({
                success: true,
                message: 'C√≥digo verificado e inscripci√≥n guardada exitosamente',
                data: inscripcionGuardada.toPublicJSON ? inscripcionGuardada.toPublicJSON() : inscripcionGuardada
            });
            
        } catch (dbError) {
            console.error('‚ùå Error guardando en base de datos:', dbError);
            
            // Manejo de errores de duplicados
            if (dbError.code === 11000) {
                global.codigosVerificacion.delete(email.toLowerCase().trim());
                
                if (dbError.keyPattern?.dni) {
                    return res.status(400).json({
                        success: false,
                        message: `El DNI ${dbError.keyValue.dni} ya est√° registrado en el sistema`
                    });
                }
                if (dbError.keyPattern?.email) {
                    return res.status(400).json({
                        success: false,
                        message: `El email ${dbError.keyValue.email} ya est√° registrado en el sistema`
                    });
                }
            }
            
            // Manejo de errores de validaci√≥n
            if (dbError.name === 'ValidationError') {
                const errores = Object.values(dbError.errors).map(err => err.message);
                return res.status(400).json({
                    success: false,
                    message: 'Error de validaci√≥n',
                    errors: errores
                });
            }
            
            res.status(500).json({
                success: false,
                message: 'Error guardando inscripci√≥n en base de datos',
                error: dbError.message
            });
        }
        
    } catch (error) {
        console.error('‚ùå Error verificando c√≥digo:', error);
        res.status(500).json({
            success: false,
            message: 'Error verificando c√≥digo',
            error: error.message
        });
    }
});

