// ENDPOINT: Verificar c√≥digo y guardar inscripci√≥n (SOLO P√öBLICO)
// ===============================
router.post('/verificar-codigo', async (req, res) => {
    try {
        const { email, codigo, datosInscripcion } = req.body;
        
        console.log('üîç P√öBLICO: Verificando c√≥digo para:', email);
        
        // Validar par√°metros
        if (!email || !codigo || !datosInscripcion) {
            return res.status(400).json({
                success: false,
                message: 'Email, c√≥digo y datos de inscripci√≥n son requeridos'
            });
        }
        
        // Buscar c√≥digo almacenado
        const datosVerificacion = global.codigosVerificacion.get(email);
        
        if (!datosVerificacion) {
            return res.status(400).json({
                success: false,
                message: 'No se encontr√≥ c√≥digo para este email o ya expir√≥'
            });
        }
        
        // Verificar expiraci√≥n (10 minutos)
        if (Date.now() > datosVerificacion.expiresAt) {
            global.codigosVerificacion.delete(email);
            return res.status(400).json({
                success: false,
                message: 'C√≥digo expirado. Solicita uno nuevo.'
            });
        }
        
        // Verificar intentos m√°ximos (3 intentos)
        if (datosVerificacion.intentos >= 3) {
            global.codigosVerificacion.delete(email);
            return res.status(400).json({
                success: false,
                message: 'Demasiados intentos fallidos. Solicita un c√≥digo nuevo.'
            });
        }
        
        // Verificar c√≥digo
        if (codigo.trim() !== datosVerificacion.codigo) {
            datosVerificacion.intentos++;
            return res.status(400).json({
                success: false,
                message: `C√≥digo incorrecto. Intentos restantes: ${3 - datosVerificacion.intentos}`
            });
        }
        
        console.log('‚úÖ P√öBLICO: C√≥digo verificado correctamente');
        
        // üéØ C√ìDIGO CORRECTO - GUARDAR INSCRIPCI√ìN EN LA BASE DE DATOS
        try {
            // Validar DNI √∫nico antes de crear
            const existeDNI = await Inscripcion.findOne({ 
                dni: datosInscripcion.dni, 
                activo: true 
            });
            if (existeDNI) {
                global.codigosVerificacion.delete(email);
                return res.status(400).json({
                    success: false,
                    message: 'Ya existe una inscripci√≥n con este DNI'
                });
            }

            // Validar email √∫nico antes de crear
            const existeEmail = await Inscripcion.findOne({ 
                email: datosInscripcion.email, 
                activo: true 
            });
            if (existeEmail) {
                global.codigosVerificacion.delete(email);
                return res.status(400).json({
                    success: false,
                    message: 'Ya existe una inscripci√≥n con este email'
                });
            }
            
            // Generar n√∫mero de equipo autom√°ticamente
            const totalInscripciones = await Inscripcion.countDocuments({ activo: true });
            const numeroEquipo = totalInscripciones + 1;
            
            // Preparar datos para la inscripci√≥n con numero generado
            const datosCompletos = {
                ...datosInscripcion,
                N_equipo: numeroEquipo,
                numeroGrupo: numeroEquipo,
                activo: true
            };
            
            const nuevaInscripcion = new Inscripcion(datosCompletos);
            await nuevaInscripcion.save();
            
            console.log('‚úÖ P√öBLICO: Inscripci√≥n guardada en BD:', nuevaInscripcion.N_equipo || nuevaInscripcion.NRO);
            
            // üéØ ENVIAR EMAIL DE CONFIRMACI√ìN (segundo email)
            try {
                await emailService.enviarConfirmacionInscripcion(email, nuevaInscripcion);
                console.log('‚úÖ Email de confirmaci√≥n enviado');
            } catch (emailError) {
                console.error('‚ö†Ô∏è Error enviando confirmaci√≥n (inscripci√≥n ya guardada):', emailError);
                // No fallar el proceso si el email de confirmaci√≥n falla
            }
            
            // Limpiar c√≥digo usado
            global.codigosVerificacion.delete(email);
            
            res.json({
                success: true,
                message: 'C√≥digo verificado e inscripci√≥n guardada exitosamente',
                data: nuevaInscripcion.toPublicJSON ? nuevaInscripcion.toPublicJSON() : nuevaInscripcion
            });
            
        } catch (dbError) {
            console.error('‚ùå Error guardando en base de datos:', dbError);
            
            // Manejar errores espec√≠ficos de la base de datos
            if (dbError.name === 'ValidationError') {
                const errores = Object.values(dbError.errors).map(err => err.message);
                return res.status(400).json({
                    success: false,
                    message: 'Error de validaci√≥n',
                    errors: errores
                });
            }
            
            // Error gen√©rico de base de datos
            return res.status(500).json({
                success: false,
                message: 'Error guardando inscripci√≥n en la base de datos',
                error: dbError.message
            });
        }
        
    } catch (error) {
        console.error('‚ùå Error verificando c√≥digo:', error);
        res.status(500).json({
            success: false,
            message: 'Error interno del servidor',
            error: error.message
        });
    }
});

