// PUT /api/inscripciones/:id - Actualizar inscripci√≥n
router.put('/:id', async (req, res) => {
    try {
        const {
            tripulante, nombres, apellidos, edad, experiencia, grupoSanguineo,
            dni, email, celular, personaContacto, celularContacto,
            grupo, contacto, frecuencia,
            tipoVehiculo, marca, modelo, a√±o, diaLlegada
        } = req.body;

        // Validar que la inscripci√≥n existe
        const inscripcionExistente = await Inscripcion.findById(req.params.id);
        if (!inscripcionExistente) {
            return res.status(404).json({ message: 'Inscripci√≥n no encontrada' });
        }

        // Validar DNI √∫nico (excluyendo el registro actual)
        const existeDNI = await Inscripcion.findOne({ 
            dni, 
            activo: true, 
            _id: { $ne: req.params.id } 
        });
        if (existeDNI) {
            return res.status(400).json({ 
                message: 'Ya existe otra inscripci√≥n con este DNI' 
            });
        }

        // Validar email √∫nico (excluyendo el registro actual)
        const existeEmail = await Inscripcion.findOne({ 
            email, 
            activo: true, 
            _id: { $ne: req.params.id } 
        });
        if (existeEmail) {
            return res.status(400).json({ 
                message: 'Ya existe otra inscripci√≥n con este email' 
            });
        }

        // Actualizar inscripci√≥n
        const inscripcionActualizada = await Inscripcion.findByIdAndUpdate(
            req.params.id,
            {
                tripulante,
                nombres: nombres.trim(),
                apellidos: apellidos.trim(),
                edad: parseInt(edad),
                experiencia,
                grupoSanguineo: grupoSanguineo.trim().toUpperCase(),
                dni: dni.trim(),
                email: email.trim().toLowerCase(),
                celular: celular.trim(),
                personaContacto: personaContacto.trim(),
                celularContacto: celularContacto.trim(),
                grupo: grupo.trim(),
                contacto: contacto.trim(),
                frecuencia: parseFloat(frecuencia),
                tipoVehiculo,
                marca: marca.trim(),
                modelo: modelo.trim(),
                a√±o: parseInt(a√±o),
                diaLlegada
            },
            { new: true, runValidators: true }
        );

        res.json({
            message: 'Inscripci√≥n actualizada exitosamente',
            inscripcion: inscripcionActualizada
        });

    } catch (error) {
        console.error('Error actualizando inscripci√≥n:', error);
        
        if (error.name === 'ValidationError') {
            const errores = Object.values(error.errors).map(err => err.message);
            return res.status(400).json({ 
                message: 'Error de validaci√≥n', 
                errores 
            });
        }
        
        res.status(500).json({ 
            message: 'Error interno del servidor', 
            error: error.message 
        });
    }
});

